import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "../../..");
const sourcePath = path.join(repoRoot, ".claude/skills/use-calendar/SKILL.md");
const outputDir = path.join(repoRoot, "apps/api/src/generated");
const outputPath = path.join(outputDir, "skill-markdown.ts");

const skillMarkdown = readFileSync(sourcePath, "utf8");

const generatedFile = `// This file is generated by apps/api/scripts/sync-skill-md.mjs.
// Do not edit manually.

export const skillMarkdown = ${JSON.stringify(skillMarkdown)};
`;

mkdirSync(outputDir, { recursive: true });
writeFileSync(outputPath, generatedFile, "utf8");
